/*
========================================================
TOPIC : DISABLE TRIGGER
========================================================
*/

CREATE OR REPLACE TRIGGER trg_before_insert
BEFORE INSERT
ON LIBRARY_BOOKS
FOR EACH ROW

BEGIN

    DBMS_OUTPUT.PUT_LINE(
        'Trigger Executed'
    );

END;
/

/*
========================================================
DISABLE TRIGGER
========================================================
*/

ALTER TRIGGER trg_before_insert
DISABLE;

/*
========================================================
TEST
========================================================
*/

INSERT INTO LIBRARY_BOOKS
VALUES
(
    201,
    'Spring Boot',
    'Rod Johnson',
    1000
);

COMMIT;

/*
========================================================
OUTPUT
========================================================

1 Row Inserted

Trigger Executed Message Will NOT Appear

========================================================
*/



/*
========================================================
TOPIC : ENABLE TRIGGER
========================================================
*/

ALTER TRIGGER trg_before_insert
ENABLE;

/*
========================================================
TEST
========================================================
*/

INSERT INTO LIBRARY_BOOKS
VALUES
(
    202,
    'Docker',
    'Docker Team',
    1500
);

COMMIT;

/*
========================================================
OUTPUT
========================================================

Trigger Executed

1 Row Inserted

========================================================
*/



