SET SERVEROUTPUT ON;

DECLARE

    v_salary NUMBER := 50000;

BEGIN

    IF v_salary > 40000 THEN

        DBMS_OUTPUT.PUT_LINE('High Salary');

    END IF;

END;
/

/*
========================================================
OUTPUT
========================================================

High Salary

========================================================
*/